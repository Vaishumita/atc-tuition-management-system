    private void btnGenerateReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateReceiptActionPerformed
        // TODO add your handling code here:
        String username = tfUserName.getText().trim();
        String date = tfDate.getText().trim();
        String receivedFrom = tfReceivedFrom.getText().trim();
        String amount = tfTheSumOf.getText().trim();
        String subjects = tfSubjects.getText().trim();
        String paymentFor = tfBeingPaymentOf.getText().trim();
        String cashCheque = tfPaymentMethod.getText().trim();
        String receivedBy = tfReceivedBy.getText().trim();

        // Create receipt text
        String receiptText = "==================== RECEIPT ====================\n"
                + "Date            : " + date + "\n"
                + "Received From   : " + receivedFrom + "\n"
                + "The Sum Of      : " + amount + "\n"
                + "Subjects        : " + subjects + "\n"
                + "Being Payment Of: " + paymentFor + "\n"
                + "Cash/Cheque No  : " + cashCheque + "\n"
                + "Received By     : " + receivedBy + "\n"
                + "=================================================\n\n";

        try {
            FileWriter writer = new FileWriter("data/receipts.txt", true); // true = append mode
            writer.write(receiptText);
            writer.close();
            JOptionPane.showMessageDialog(this, "✅ Receipt successfully saved to receipts.txt!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "❌ Error saving receipt: " + e.getMessage());
        }

    }//GEN-LAST:event_btnGenerateReceiptActionPerformed

