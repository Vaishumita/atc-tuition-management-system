    private void generateReceipt() {
    String username = tfUserName.getText().trim();
    File file = new File("data/students.txt");

    try (BufferedReader br = new BufferedReader(new FileReader(file))) {
        String line;
        br.readLine(); // skip header

        while ((line = br.readLine()) != null) {
            String[] parts = line.split(",");

            if (parts.length >= 13 && parts[1].equalsIgnoreCase(username)) {
                // Found student
                String fullName = parts[3];
                String date = java.time.LocalDate.now().toString();
                String subject1 = parts[10];
                String subject2 = parts[11];
                String subject3 = parts[12];
                String address = parts[8];
                String phone = parts[7];
                String receivedBy = "Receptionist";

                // Assume each subject = RM 100
                int total = 0;
                if (!subject1.isEmpty()) total += 100;
                if (!subject2.isEmpty()) total += 100;
                if (!subject3.isEmpty()) total += 100;

                tfReceivedFrom.setText(fullName);
                tfDate.setText(date);
                tfTheSumOf.setText("RM " + total);
                tfSubjects.setText(subject1 + ", " + subject2 + ", " + subject3);
                tfPaymentMethod.setText("Cash"); // you can let user enter this
                tfReceivedBy.setText(receivedBy);
                tfSign.setText(receivedBy);
                break;
            }
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error reading student file!");
        e.printStackTrace();
        }
    }
    


    
    
