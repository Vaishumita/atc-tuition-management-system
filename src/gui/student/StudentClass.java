    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:   
    String username = lblName.getText().trim(); // Get current student username
    DefaultTableModel model = (DefaultTableModel) tblViewPayment.getModel();
    model.setRowCount(0); // clear previous rows

    String[] subjects = null;
    int subjectCount = 0;

    // Step 1: Read from students.txt to get subjects
    try (BufferedReader br = new BufferedReader(new FileReader("students.txt"))) {
        String line;
        boolean found = false;
        while ((line = br.readLine()) != null) {
            String[] data = line.split(",");
            if (data.length >= 12 && data[1].equalsIgnoreCase(username)) {
                subjects = new String[]{data[9], data[10], data[11]};
                subjectCount = subjects.length;
                found = true;
                break;
            }
        }
        if (!found) {
            JOptionPane.showMessageDialog(this, "No subjects found for user.");
            return;
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Student file not found.");
        return;
    }

    // Step 2: Read from payments.txt to get amount paid and date
    double amountPaid = 0.0;
    String paymentDate = "N/A";
    try (BufferedReader br = new BufferedReader(new FileReader("payments.txt"))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] data = line.split(",");
            if (data.length >= 3 && data[0].equalsIgnoreCase(username)) {
                amountPaid = Double.parseDouble(data[1]);
                paymentDate = data[3];
                break;
            }
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Payment file not found.");
        return;
    }

    // Step 3: Calculate total fee (Assume RM125 per subject)
    int totalFee = subjectCount * 125;
    double balance = totalFee - amountPaid;

    // Step 4: Format subject list (each in new line)
    String subjectList = "";
    for (String s : subjects) {
        if (s != null && !s.trim().isEmpty()) {
            subjectList += s + "\n";
        }
    }

    // Step 5: Add to table
    model.addRow(new Object[]{
        paymentDate,
        subjectList.trim(),
        "RM " + totalFee,
        "RM " + amountPaid,
        "RM " + balance
    });
    }//GEN-LAST:event_btnViewActionPerformed

