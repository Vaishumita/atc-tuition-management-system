    private void btnRemovePendingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemovePendingActionPerformed
        // TODO add your handling code here:
            String username = UserSession.getUsername();

            File inputFile = new File("data/pending_requests.txt");
            File tempFile = new File("data/temp_requests.txt");

            try (
                BufferedReader reader = new BufferedReader(new FileReader(inputFile));
                BufferedWriter writer = new BufferedWriter(new FileWriter(tempFile));
            ) {
                String currentLine;

                while ((currentLine = reader.readLine()) != null) {
                    // Skip the lines belonging to this user
                    if (!currentLine.startsWith(username + ",")) {
                        writer.write(currentLine);
                        writer.newLine();
                    }
                }

                reader.close();
                writer.close();

                if (inputFile.delete()) {
                    tempFile.renameTo(inputFile);
                    JOptionPane.showMessageDialog(null, "Pending request removed!");
                }
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
    }//GEN-LAST:event_btnRemovePendingActionPerformed

