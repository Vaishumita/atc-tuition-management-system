    private void chkRememberMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkRememberMeActionPerformed
        // TODO add your handling code here:
            if (chkRememberMe.isSelected()) {
                String currentusername = tfUserName.getText().trim();
                String currentpassword = new String(tfPassword.getPassword()).trim();

                if (!currentusername.isEmpty() && !currentpassword.isEmpty()) {
                    try {
                        File rememberFile = new File("data/rememberme.txt");
                        BufferedWriter writer = new BufferedWriter(new FileWriter(rememberFile));
                        writer.write(currentusername + "," + password);
                        writer.close();
                        JOptionPane.showMessageDialog(this, "Credentials will be remembered ✅");
                    } catch (IOException e) {
                        JOptionPane.showMessageDialog(this, "Failed to save Remember Me file ❌\n" + e.getMessage());
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Please fill both username and password fields first.");
                    chkRememberMe.setSelected(false); // auto-uncheck if fields empty
                }
            } else {
                // If user unchecks it, remove the file
                File rememberFile = new File("data/rememberme.txt");
                if (rememberFile.exists()) {
                    rememberFile.delete();
                    JOptionPane.showMessageDialog(this, "Remember Me disabled. Info cleared ✅");
                }
           }
    }//GEN-LAST:event_chkRememberMeActionPerformed

