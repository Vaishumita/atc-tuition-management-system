    private void loginbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbuttonActionPerformed
        // TODO add your handling code here:
String Username = tfUserName.getText().trim();
UserSession.setUsername(Username);
String Password = new String(tfPassword.getPassword()).trim();

boolean isLoggedIn = false;
String role = "";

try (BufferedReader reader = new BufferedReader(new FileReader("data/users.txt"))) {
    String line;
    while ((line = reader.readLine()) != null) {
        String[] parts = line.split(",");
        if (parts.length >= 3) {
            String fileUsername = parts[0].trim();
            String filePassword = parts[1].trim();
            String fileRole     = parts[2].trim();

            if (Username.equals(fileUsername) && Password.equals(filePassword)) {
                isLoggedIn = true;
                role = fileRole;
                break; // Exit loop once user is found
            }
        }
    }
} catch (IOException e) {
    JOptionPane.showMessageDialog(this, "Error reading users.txt:\n" + e.getMessage());
}

// ✅ Now handle result AFTER the loop
if (isLoggedIn) {
    JOptionPane.showMessageDialog(this, "Login successful as " + role + "!");

    switch (role.toLowerCase()) {
        case "admin":
            new AdminPage().setVisible(true);
            break;
        case "tutor":
            new TutorPage().setVisible(true);
            break;
        case "receptionist":
            new ReceptionistPage().setVisible(true);
            break;
        case "student":
            new StudentPage().setVisible(true);
            break;
        default:
            JOptionPane.showMessageDialog(this, "Unknown role: " + role);
            break;
    }

    this.dispose(); // Close login window
} else {
    JOptionPane.showMessageDialog(this, "Invalid username or password.");
    }
    }//GEN-LAST:event_loginbuttonActionPerformed

